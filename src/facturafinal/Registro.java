    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        int a=0;
        String correo = txt_correo.getText().trim();
        String contraseña = txt_contraseña.getText().trim();
        if(txt_correo.getText().isEmpty() || txt_contraseña.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Algunos de los campos del correo o contraseña estan vacios");
        }else{
            if(Correol.size()==0){
                Correol.add(correo);
                Contraseñal.add(contraseña);
                JOptionPane.showMessageDialog(null, "Datos guardados");
            }else{
                for (int i = 0; i < Correol.size(); i++) {
                    if(correo.equals(Correol.get(i))){
                        JOptionPane.showMessageDialog(null, "El correo que intenta registrar ya se encuentra activo");
                    }else{
                        Correol.add(correo);
                        Contraseñal.add(contraseña);
                        JOptionPane.showMessageDialog(null, "Datos guardados");
                    }
                }
            }
        }
        txt_correo.setText("");
        txt_contraseña.setText("");
    }//GEN-LAST:event_jButton2ActionPerformed

